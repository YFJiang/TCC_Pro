void X86LinuxBackend::emitPrologue() {
    emitLine("\tpushl %ebp");
    emitLine("\tpushl %ebx");
    emitLine("\tpushl %esi");
    emitLine("\tpushl %edi");
    emitLine("\tmovl %esp, %ebp");
    emitLine("\tsubl $256, %esp");  // Reserve 256 bytes for locals
}

void X86LinuxBackend::emitEpilogue() {
    emitLine("\tmovl %ebp, %esp");
    emitLine("\tpopl %edi");
    emitLine("\tpopl %esi");
    emitLine("\tpopl %ebx");
    emitLine("\tpopl %ebp");
    emitLine("\tret");
}

void X86LinuxBackend::emitFunction(Symbol* func) {
    auto* fsym = static_cast<FunctionSymbol*>(func);
    stackSlots_.clear();
    stackOffset_ = -4;
    export_(func);
    emitLine(std::string(getAccessName(func)) + ":");
    emitPrologue();
    emitFunctionBody(fsym);
    emitEpilogue();
    emitLine("");
}

void X86LinuxBackend::emitFunctionBody(FunctionSymbol* fsym) {
    BBlock* bb = reinterpret_cast<BBlock*>(fsym->entryBB);
    while (bb) {
        // Create and emit label for blocks that have incoming edges
        if (bb->npred > 0 || bb != reinterpret_cast<BBlock*>(fsym->entryBB)) {
            if (!bb->sym) bb->sym = syms_.createLabel();
        }
        if (bb->sym) {
            emitLine(std::string(getAccessName(bb->sym)) + ":");
        }
        auto* inst = bb->insth.next;
        while (inst != &bb->insth) {
            emitIR(inst);
            inst = inst->next;
        }
        bb = bb->next;
    }
}

void X86LinuxBackend::emitIR(IRInst* inst) {
    char buf[256];
    switch (inst->opcode) {
    case OpCode::ADDR: {
        auto* src = inst->opds[1];
        if (src->kind == SK_String) {
            snprintf(buf, sizeof(buf), "\tleal %s, %%eax", getAccessName(src));
            emitLine(buf);
            snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        } else {
            snprintf(buf, sizeof(buf), "\tleal %s, %s",
                getAccessName(src), getAccessName(inst->opds[0]));
        }
        break;
    }
    case OpCode::MOV: {
        auto* dst = inst->opds[0];
        auto* src = inst->opds[1];
        if (src->kind == SK_Constant) {
            // Immediate to register/stack
            snprintf(buf, sizeof(buf), "\tmovl $%d, %s", src->val.i[0], getAccessName(dst));
        } else if (src->kind == SK_String) {
            // String address (leal)
            snprintf(buf, sizeof(buf), "\tleal %s, %%eax", getAccessName(src));
            emitLine(buf);
            snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(dst));
        } else {
            snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(src));
            emitLine(buf);
            snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(dst));
        }
        break;
    }
    case OpCode::ADD:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\taddl %s, %%eax", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::SUB:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tsubl %s, %%eax", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::MUL:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\timull %s, %%eax", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::DIV:
        emitLine("\tmovl $0, %edx");
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tdivl %s", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::MOD:
        emitLine("\tmovl $0, %edx");
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tdivl %s", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%edx, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::BOR:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\torl %s, %%eax", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::BAND:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tandl %s, %%eax", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::BXOR:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\txorl %s, %%eax", getAccessName(inst->opds[2]));
        emitLine(buf);
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        break;
    case OpCode::NEG:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[1]));
        emitLine(buf);
        emitLine("\tnegl %eax");
        snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
        return;
    case OpCode::CALL: {
        // Push arguments in reverse order (args stored in opds[2] as vector ptr)
        auto* faddr = inst->opds[1];
        const char* fname = faddr->name ? faddr->name : getAccessName(faddr);

        // Emit arguments as pushes
        if (inst->opds[2]) {
            auto* args = reinterpret_cast<std::vector<Symbol*>*>(inst->opds[2]);
            for (int i = static_cast<int>(args->size()) - 1; i >= 0; i--) {
                auto* arg = (*args)[i];
                if (arg->kind == SK_Constant) {
                    snprintf(buf, sizeof(buf), "\tpushl $%d", arg->val.i[0]);
                } else if (arg->kind == SK_String) {
                    snprintf(buf, sizeof(buf), "\tleal %s, %%eax", getAccessName(arg));
                    emitLine(buf);
                    emitLine("\tpushl %eax");
                    continue;
                } else {
                    snprintf(buf, sizeof(buf), "\tpushl %s", getAccessName(arg));
                }
                emitLine(buf);
            }
        }

        snprintf(buf, sizeof(buf), "\tcall %s", fname);
        emitLine(buf);

        // Clean up stack: addl $n, %esp where n = 4 * num_args
        if (inst->opds[2]) {
            auto* args = reinterpret_cast<std::vector<Symbol*>*>(inst->opds[2]);
            int stackSize = static_cast<int>(args->size()) * 4;
            if (stackSize > 0) {
                snprintf(buf, sizeof(buf), "\taddl $%d, %%esp", stackSize);
                emitLine(buf);
            }
        }

        if (inst->opds[0]) {
            snprintf(buf, sizeof(buf), "\tmovl %%eax, %s", getAccessName(inst->opds[0]));
            emitLine(buf);
        }
        return;
    }
    case OpCode::RET:
        snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(inst->opds[0]));
        break;
    case OpCode::JMP: {
        auto* target = reinterpret_cast<BBlock*>(inst->opds[0]);
        if (!target->sym) {
            target->sym = syms_.createLabel();
        }
        snprintf(buf, sizeof(buf), "\tjmp %s", getAccessName(target->sym));
        break;
    }
    case OpCode::JE: case OpCode::JNE:
    case OpCode::JG: case OpCode::JL:
    case OpCode::JGE: case OpCode::JLE:
    case OpCode::JZ: case OpCode::JNZ: {
        auto* target = reinterpret_cast<BBlock*>(inst->opds[0]);
        if (!target->sym) target->sym = syms_.createLabel();
        auto* s1 = inst->opds[1];
        auto* s2 = inst->opds[2];
        if (s2) {
            // Load one operand into register for cmp
            snprintf(buf, sizeof(buf), "\tmovl %s, %%eax", getAccessName(s2));
            emitLine(buf);
            snprintf(buf, sizeof(buf), "\tcmpl %%eax, %s", getAccessName(s1));
            emitLine(buf);
        } else {
            snprintf(buf, sizeof(buf), "\tcmpl $0, %s", getAccessName(s1));
            emitLine(buf);
        }
        const char* jcc;
        switch (inst->opcode) {
        case OpCode::JE: jcc = "je"; break;
        case OpCode::JNE: case OpCode::JNZ: jcc = "jne"; break;
        case OpCode::JG: jcc = "jg"; break;
        case OpCode::JL: jcc = "jl"; break;
        case OpCode::JGE: jcc = "jge"; break;
        case OpCode::JLE: jcc = "jle"; break;
        case OpCode::JZ: jcc = "je"; break;
        default: jcc = "je"; break;
        }
        snprintf(buf, sizeof(buf), "\t%s %s", jcc, getAccessName(target->sym));
        break;
    }
    default:
        snprintf(buf, sizeof(buf), "\t# opcode %d", static_cast<int>(inst->opcode));
        break;
    }
    emitLine(buf);
}

