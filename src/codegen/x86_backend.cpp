X86LinuxBackend::X86LinuxBackend(SymbolTable& syms, TypeContext& types)
    : syms_(syms), types_(types) {}

void X86LinuxBackend::emit(const std::string& s) { out_ << s; }
void X86LinuxBackend::emit(const char* s) { out_ << s; }
void X86LinuxBackend::emitLine(const std::string& s) { out_ << s; if (s.back() != '\n') out_ << "\n"; }
void X86LinuxBackend::beginProgram() {
    org_ = 0; floatNum_ = 0; tempNum_ = 0;
    emitLine("# Code auto-generated by TCC_Pro\n");
}
void X86LinuxBackend::endProgram() {}
std::string X86LinuxBackend::getOutput() { return out_.str(); }

void X86LinuxBackend::segment(int seg) {
    if (seg == DATA) emitLine(".data\n");
    else if (seg == CODE) emitLine(".text\n");
}

void X86LinuxBackend::import(Symbol* p) { (void)p; }
void X86LinuxBackend::export_(Symbol* p) {
    char buf[256];
    snprintf(buf, sizeof(buf), ".globl\t%s\n", getAccessName(p));
    emitLine(buf);
    emitLine("");
}

const char* X86LinuxBackend::getAccessName(Symbol* p) {
    if (p->aname) return p->aname;

    switch (p->kind) {
    case SK_Constant: {
        char* buf = new char[64];
        snprintf(buf, 64, "$%s", p->name);
        p->aname = buf; break;
    }
    case SK_String: case SK_Label: {
        char* buf = new char[64];
        snprintf(buf, 64, ".%s", p->name);
        p->aname = buf; break;
    }
    case SK_Variable: case SK_Temp: {
        auto* v = static_cast<VariableSymbol*>(p);
        if (p->kind == SK_Function) {
            p->aname = p->name;
        } else if (p->sclass == 3 && p->level > 0) {
            char* buf = new char[64];
            snprintf(buf, 64, "%s.%d", p->name, tempNum_++);
            p->aname = buf;
        } else if (p->level > 0 || p->kind == SK_Temp) {
            // Assign unique stack offset
            int offset;
            auto it = stackSlots_.find(p);
            if (it != stackSlots_.end()) {
                offset = it->second;
            } else {
                offset = stackOffset_;
                stackOffset_ -= 4;
                stackSlots_[p] = offset;
            }
            v->offset = offset;
            char* buf = new char[64];
            snprintf(buf, 64, "%d(%%ebp)", offset);
            p->aname = buf;
        } else {
            p->aname = p->name;
        }
        break;
    }
    case SK_Function:
        p->aname = p->name; break;
    case SK_Offset: {
        auto* base = p->link;
        auto* v = static_cast<VariableSymbol*>(p);
        int n = v->offset;
        if (base->level == 0 || base->sclass == 3) {
            char* buf = new char[128];
            snprintf(buf, 128, "%s%s%d", getAccessName(base), n >= 0 ? "+" : "", n);
            p->aname = buf;
        } else {
            n += static_cast<VariableSymbol*>(base)->offset;
            char* buf = new char[64];
            snprintf(buf, 64, "%d(%%ebp)", n);
            p->aname = buf;
        }
        break;
    }
    default: p->aname = "???"; break;
    }
    return p->aname;
}

void X86LinuxBackend::defineGlobal(Symbol* p) {
    if (p->sclass != 3) export_(p);
    char buf[256];
    snprintf(buf, sizeof(buf), "%s:", getAccessName(p));
    emitLine(buf);
}

void X86LinuxBackend::defineCommData(Symbol* p) {
    getAccessName(p);
    char buf[256];
    if (p->sclass == 3)
        snprintf(buf, sizeof(buf), ".lcomm\t%s,%d\n", p->aname, p->ty->size);
    else
        snprintf(buf, sizeof(buf), ".comm\t%s,%d\n", p->aname, p->ty->size);
    emit(buf);
}

struct StrBuf { char* chs; int len; };

void X86LinuxBackend::defineString(void* str, int) {
    auto* s = static_cast<StrBuf*>(str);
    int size = s->len + 1;  // include null terminator
    if (s->chs[size] == 0) { emit("\t.string\t\""); size--; }
    else emit("\t.ascii\t\"");
    for (int i = 0; i < size; i++) {
        if (!isprint(s->chs[i])) {
            char buf[16];
            snprintf(buf, sizeof(buf), "\\%03o", (unsigned char)s->chs[i]);
            emit(buf);
        } else {
            if (s->chs[i] == '"') emit("\\\"");
            else if (s->chs[i] == '\\') emit("\\\\");
            else { char b[2] = {s->chs[i], 0}; emit(b); }
        }
    }
    emitLine("\"");
}

void X86LinuxBackend::defineFloatConstant(Symbol* p) {
    char buf[64];
    snprintf(buf, sizeof(buf), ".flt%d", floatNum_++);
    p->aname = strdup(buf);
    emit(p->aname);
    emitLine(":");
    defineValue(p->ty, p->val);
}

void X86LinuxBackend::defineAddress(Symbol* p) {
    char buf[256];
    snprintf(buf, sizeof(buf), "\t.long\t%s\n", getAccessName(p));
    emit(buf);
}

void X86LinuxBackend::defineValue(Type* ty, Value val) {
    auto tc = types_.typeCode(ty);
    char buf[128];
    switch (tc) {
    case I1: case U1: snprintf(buf, sizeof(buf), "\t.byte\t%d\n", val.i[0] & 0xff); break;
    case I2: case U2: snprintf(buf, sizeof(buf), "\t.word\t%d\n", val.i[0] & 0xffff); break;
    case I4: case U4: snprintf(buf, sizeof(buf), "\t.long\t%d\n", val.i[0]); break;
    case F4: snprintf(buf, sizeof(buf), "\t.long\t%d\n", *(unsigned*)&val.f); break;
    case F8: {
        emit("\t.long\t");
        auto* p = (unsigned*)&val.d;
        char tmp[128];
        snprintf(tmp, sizeof(tmp), "%d\n\t.long\t%d\n", p[0], p[1]);
        emit(tmp);
        return;
    }
    default: return;
    }
    emit(buf);
}

